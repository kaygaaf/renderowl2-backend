// Template represents a video template
type Template struct {
	ID           string          `json:"id"`
	Name         string          `json:"name"`
	Description  string          `json:"description"`
	Category     string          `json:"category"`
	Thumbnail    string          `json:"thumbnail"`
	Gradient     string          `json:"gradient"`
	Icon         string          `json:"icon"`
	Duration     float64         `json:"duration"`
	Width        int             `json:"width"`
	Height       int             `json:"height"`
	FPS          int             `json:"fps"`
	Scenes       []TemplateScene `json:"scenes,omitempty"`
	Popularity   int             `json:"popularity"`
	Version      int             `json:"version"`
	IsActive     bool            `json:"isActive"`
	Tags         []string        `json:"tags,omitempty"`
	CreatedAt    time.Time       `json:"createdAt"`
	UpdatedAt    time.Time       `json:"updatedAt"`
}

// TemplateScene represents a scene within a template
type TemplateScene struct {
	ID          string                 `json:"id"`
	Name        string                 `json:"name"`
	Description string                 `json:"description"`
	Duration    float64                `json:"duration"`
	Order       int                    `json:"order"`
	Clips       []TemplateClip         `json:"clips,omitempty"`
	Transitions *TemplateTransition    `json:"transitions,omitempty"`
	Settings    map[string]interface{} `json:"settings,omitempty"`
}

// TemplateClip represents a clip configuration in a template scene
type TemplateClip struct {
	ID          string                 `json:"id"`
	Name        string                 `json:"name"`
	Type        string                 `json:"type"` // video, audio, image, text
	SourceType  string                 `json:"sourceType"` // url, placeholder, uploaded
	SourceURL   string                 `json:"sourceUrl,omitempty"`
	Placeholder string                 `json:"placeholder,omitempty"`
	StartTime   float64                `json:"startTime"`
	EndTime     float64                `json:"endTime"`
	PositionX   float64                `json:"positionX"`
	PositionY   float64                `json:"positionY"`
	Scale       float64                `json:"scale"`
	Rotation    float64                `json:"rotation"`
	Opacity     float64                `json:"opacity"`
	TextContent string                 `json:"textContent,omitempty"`
	TextStyle   *TemplateTextStyle     `json:"textStyle,omitempty"`
	Settings    map[string]interface{} `json:"settings,omitempty"`
}

// TemplateTextStyle represents text styling for template clips
type TemplateTextStyle struct {
	FontSize        int     `json:"fontSize"`
	FontFamily      string  `json:"fontFamily"`
	Color           string  `json:"color"`
	Background      string  `json:"background,omitempty"`
	BackgroundColor string  `json:"backgroundColor,omitempty"`
	Bold            bool    `json:"bold"`
	Italic          bool    `json:"italic"`
	Alignment       string  `json:"alignment"`
	LineHeight      float64 `json:"lineHeight,omitempty"`
	LetterSpacing   float64 `json:"letterSpacing,omitempty"`
}

// TemplateTransition represents transition settings
type TemplateTransition struct {
	Type     string  `json:"type"` // fade, slide, zoom, wipe, none
	Duration float64 `json:"duration"`
	Easing   string  `json:"easing,omitempty"`
}

// UseTemplateRequest represents a request to use a template
type UseTemplateRequest struct {
	Name        string                 `json:"name"`
	Description string                 `json:"description,omitempty"`
	CustomData  map[string]interface{} `json:"customData,omitempty"` // For replacing placeholders
}

// UseTemplateResponse represents the response after using a template
type UseTemplateResponse struct {
	TimelineID string    `json:"timelineId"`
	Message    string    `json:"message"`
	TemplateID string    `json:"templateId"`
	CreatedAt  time.Time `json:"createdAt"`
}

// TemplateFilter represents filter options for listing templates
type TemplateFilter struct {
	Category string
	Search   string
	Limit    int
	Offset   int
}

// ScenesJSON is a helper type for JSON serialization
type ScenesJSON []TemplateScene

// Value implements driver.Valuer for database storage
func (s ScenesJSON) Value() (interface{}, error) {
	return json.Marshal(s)
}

// Scan implements sql.Scanner for database retrieval
func (s *ScenesJSON) Scan(value interface{}) error {
	if value == nil {
		*s = ScenesJSON{}
		return nil
	}
	bytes, ok := value.([]byte)
	if !ok {
		return nil
	}
	return json.Unmarshal(bytes, s)
}

// TagsJSON is a helper type for tags JSON serialization
type TagsJSON []string

// Value implements driver.Valuer
func (t TagsJSON) Value() (interface{}, error) {
	return json.Marshal(t)
}

// Scan implements sql.Scanner
func (t *TagsJSON) Scan(value interface{}) error {
	if value == nil {
		*t = TagsJSON{}
		return nil
	}
	bytes, ok := value.([]byte)
	if !ok {
		return nil
	}
	return json.Unmarshal(bytes, t)
}

